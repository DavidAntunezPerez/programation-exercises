package LearnExamples;

/**
 * @author David Antunez Perez
 *  do {
    sentencias
    } while (expresion)
 */
public class UseDoWhile {
    public static void main(String[] args) {
        int n = 1;
        do{
            System.out.println(n);
            n++;
        }while (n < 10);
        
    }
}
